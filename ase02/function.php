<?php


function penjumlahan($a, $b)
{
    return $a + $b;
}

function pengurangan($a, $b)
{
    return $a - $b;
}

function pengalian($a, $b)
{
    return $a * $b;
}

function pembagian($a, $b)
{
    return $a / $b;
}

// cara menggunakan fungsi

echo "penjumlahan dari 3 dan 4 adalah : " . penjumlahan(3, 4) . "\n";
echo "pengurangan dari 7 dan 2 adalah : " . pengurangan(7, 2) . "\n";
echo "pengalian dari 23 dan 1 adalah : " . pengalian(23, 1) . "\n";
echo "pembagian dari 3 dan 4 adalah : " . pembagian(3, 4) . "\n";
